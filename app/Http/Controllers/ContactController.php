     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Contact  $contact
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $contact = Contact::findOrFail($id);

        if($contact){
            $contact->delete();

            $notification= array(
                'message' => 'Message deleted successfully',
                'alert-type' => 'error'

            );
        }
